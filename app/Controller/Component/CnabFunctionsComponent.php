	function zerosEsq($campo, $tamanho){
		$campo = $this->tira_caracteres($campo);
		$campo = substr($campo,0,$tamanho);

		$cp = str_pad($campo, $tamanho, 0, STR_PAD_LEFT);
		return $cp;
	}

	function geraEspacos($qtde){
		$sp = str_pad('',$qtde);
		return $sp; 
	}

	function completaComEspaco($campo, $tamanho){
		$campo = $this->tira_caracteres($campo);
		$campo = substr($campo,0,$tamanho);
		
		$cp = str_pad($campo, $tamanho);

		return $cp;
	}

	function DataArquivo($line){    	
		return "20" . substr($line,98,2) . "-" . substr($line,96,2) . "-" . substr($line,94,2);
	}

	function SeqArquivo($line){  
		return $this->leftZero(trim(substr($line,108,5)),6);
	}

	function KeyArquivo($line){
		return trim(substr($line,79,15));
	}

	function ContaArquivo($line){
    return intval(substr($line, 58, 12));    
  }

	function TipoArquivo($line){
		return trim(substr($line,2,7));
	}

	function leftZero($iNum, $qtdeZero){
		$var = substr($iNum, 0, $qtdeZero);
		$ini = strlen($var);

		for($i = $ini;$i < $qtdeZero;$i++) {
			$var = '0'.$var;
		}

		return $var;
	}

	function isEmpty($str) {
		return (strlen(trim($str)) == 0) || (strtolower($str) == "null");
